//Conditional Statement

// if Statement 

var votingAge = 18;

var yourAge = 19;

if (yourAge >= votingAge) {

    console.log("You are eligible to vote.")
}


// if-else Statement

const drivingAge = 18;

const yourAge = 17;

if (yourAge >= drivingAge) {

    console.log(`You are eligible for driving licence.`)

}  else {

    const yearLeft = drivingAge - yourAge

    console.log(`You are not eligible for driving licence. Year left for eligibility is ${yearLeft}.`)
}

